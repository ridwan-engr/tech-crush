//DATA TYPES
//PRIMITIVE DATA TYPES
//STRING: A TEXT ENCLOSED IN QOUTES
//var, let and const are used to declare variable in Javascript.
var person = "John";
console.log(person);
let myName = "Ridwan";
console.log(myName);

//NUMBER: A NUMERIC VALUE
let age = 30;
const pi = 3.14;
console.log(age);
console.log(pi);

//BOOLEAN: A VALUE THAT CAN EITHER TRUE OR FALSE
const isStudent = true;
